import { v } from "convex/values";
import { mutation, query } from "./_generated/server";
import { paginationOptsValidator } from "convex/server";

// Get all chats ordered by updatedAt
export const list = query({
  args: { paginationOpts: paginationOptsValidator },
  handler: async (ctx, args) => {
    const chats = await ctx.db
      .query("chats")
      .withIndex("by_updated")
      .order("desc")
      .paginate(args.paginationOpts);

    // Get message counts for each chat
    const page = await Promise.all(
      chats.page.map(async (chat) => {
        const messages = await ctx.db
          .query("messages")
          .withIndex("by_chat", (q) => q.eq("chatId", chat._id))
          .collect();
        return {
          ...chat,
          messageCount: messages.length,
        };
      })
    );

    return { ...chats, page };
  },
});

// Get a single chat with its messages
export const get = query({
  args: { chatId: v.id("chats") },
  handler: async (ctx, args) => {
    const chat = await ctx.db.get(args.chatId);
    if (!chat) return null;

    return chat;
  },
});

// Create a new chat
export const create = mutation({
  args: {
    title: v.optional(v.string()),
  },
  handler: async (ctx, args) => {
    const now = Date.now();
    const chatId = await ctx.db.insert("chats", {
      title: args.title || "New chat",
      createdAt: now,
      updatedAt: now,
    });
    return chatId;
  },
});

// Update chat title
export const updateTitle = mutation({
  args: {
    chatId: v.id("chats"),
    title: v.string(),
  },
  handler: async (ctx, args) => {
    await ctx.db.patch(args.chatId, {
      title: args.title,
      updatedAt: Date.now(),
    });
  },
});

// Delete a chat and its messages
export const remove = mutation({
  args: { chatId: v.id("chats") },
  handler: async (ctx, args) => {
    // Delete all messages first
    const messages = await ctx.db
      .query("messages")
      .withIndex("by_chat", (q) => q.eq("chatId", args.chatId))
      .collect();

    for (const message of messages) {
      await ctx.db.delete(message._id);
    }

    // Delete the chat
    await ctx.db.delete(args.chatId);
  },
});

// Fork a chat from a specific message
export const fork = mutation({
  args: {
    chatId: v.id("chats"),
    messageIndex: v.number(),
  },
  handler: async (ctx, args) => {
    const sourceChat = await ctx.db.get(args.chatId);
    if (!sourceChat) throw new Error("Chat not found");

    const messages = await ctx.db
      .query("messages")
      .withIndex("by_chat", (q) => q.eq("chatId", args.chatId))
      .order("asc")
      .collect();

    const now = Date.now();

    // Create new forked chat
    const newChatId = await ctx.db.insert("chats", {
      title: `Fork: ${sourceChat.title}`,
      createdAt: now,
      updatedAt: now,
      parentId: args.chatId,
      forkMessageIndex: args.messageIndex,
    });

    // Copy messages up to the fork point
    const messagesToCopy = messages.slice(0, args.messageIndex + 1);
    for (const msg of messagesToCopy) {
      await ctx.db.insert("messages", {
        chatId: newChatId,
        role: msg.role,
        content: msg.content,
        createdAt: msg.createdAt,
      });
    }

    // Update fork count on source chat
    await ctx.db.patch(args.chatId, {
      forkCount: (sourceChat.forkCount || 0) + 1,
    });

    return newChatId;
  },
});
